    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        
        
        
        if((Integer)jSpinner1.getValue() > 0 && (Integer)jSpinner1.getValue() <=10 &&
            (Integer) jSpinner2.getValue()>0 && (Integer)jSpinner2.getValue() <=10 &&
                
                Integer.parseInt(jTextField3.getText()) >0 && Integer.parseInt(jTextField3.getText())<=100 &&
                
                Integer.parseInt(jTextField1.getText()) >=0 && Integer.parseInt(jTextField1.getText())<=10000 &&
                Integer.parseInt(jTextField2.getText()) >=0 && Integer.parseInt(jTextField2.getText())<=10000&&
                
                Integer.parseInt(jTextField4.getText()) >=0 && Integer.parseInt(jTextField3.getText())<10 &&
                (Integer) jSpinner2.getValue()>=0 && (Integer)jSpinner2.getValue() <10){
            
            Buffer buffer = new Buffer(Integer.parseInt(jTextField3.getText()),this);
            int value = (Integer) jSpinner1.getValue();
            int valueC = (Integer) jSpinner2.getValue();
        
            int esperaP = Integer.parseInt(jTextField1.getText());
            int esperaC = Integer.parseInt(jTextField2.getText());
        
            int rangoUno = Integer.parseInt(jTextField4.getText());
            int rangoDos = (Integer)jSpinner3.getValue();
            
            
            for (int i = 0; i < value; i++) {
                productores.add(new Producer(buffer, i, rangoUno, rangoDos, esperaP, this));
            }

            for (int i = 0; i < valueC; i++) {
                consumidores.add(new Consumer(buffer, i, esperaC, this));
            }

            for (Producer p : productores) {
                p.start();
            }

            for (Consumer c : consumidores) {
                c.start();
            }

            tableModel.addColumn("ID Producer          Operacion");
            jTable1.setModel(tableModel);

            tableModel2.addColumn("ID Consumer");
            tableModel2.addColumn("Tarea");
            tableModel2.addColumn("Resultado");
            jTable2.setModel(tableModel2);
            
            
        }
        
        if((Integer)jSpinner1.getValue() <= 0 || (Integer)jSpinner1.getValue() >10 
            ){
            JOptionPane.showMessageDialog(null,
                "Productor Incorrecto introduce valores del 1-10",
                "Error productor",
                JOptionPane.ERROR_MESSAGE);
        }
        if((Integer) jSpinner2.getValue()<=0 || (Integer)jSpinner2.getValue() >10 
            ){
            JOptionPane.showMessageDialog(null,
                "Consumidor Incorrecto introduce valores del 1-10",
                "Error consumidor",
                JOptionPane.ERROR_MESSAGE);
        }
        
        if(Integer.parseInt(jTextField3.getText()) <0 || Integer.parseInt(jTextField3.getText())>100  
            ){
            JOptionPane.showMessageDialog(null,
                "Introduce un buffer del 1 al 100",
                "Error tama침o del buffer",
                JOptionPane.ERROR_MESSAGE);
        }
        
        if(Integer.parseInt(jTextField1.getText()) <0 || Integer.parseInt(jTextField1.getText())>10000  
            ){
            JOptionPane.showMessageDialog(null,
                "Tiempo de espera productor del 0 al 10000",
                "Error espera productor",
                JOptionPane.ERROR_MESSAGE);
        }
        
        if(Integer.parseInt(jTextField2.getText()) <0 || Integer.parseInt(jTextField2.getText())>10000  
            ){
            JOptionPane.showMessageDialog(null,
                "Tiempo de espera consumidor del 0 al 10000",
                "Error espera consumidor",
                JOptionPane.ERROR_MESSAGE);
        }
        if(Integer.parseInt(jTextField4.getText()) <0 || Integer.parseInt(jTextField4.getText())>9  
            ){
            JOptionPane.showMessageDialog(null,
                "rango de valores del 0 al 9",
                "Error rango de valores min ",
                JOptionPane.ERROR_MESSAGE);
        }
        if((Integer) jSpinner3.getValue()<0 || (Integer)jSpinner3.getValue() >9  
            ){
            JOptionPane.showMessageDialog(null,
                "rango de valores del 0 al 9",
                "Error rango de valores max ",
                JOptionPane.ERROR_MESSAGE);
        }
        
        
         
        
        
        
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

